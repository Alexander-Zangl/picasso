import os.path
import numpy as np
import yaml
import tifffile
import glob


class FileFormatNotSupported(Exception):
    pass


def load_raw(path, memory_map=True):
    info = load_raw_info(path)
    if memory_map:
        movie = np.memmap(path, info['data type'], 'r', shape=tuple(info['shape']))
    else:
        movie = np.fromfile(path, info['data type'])
        movie = np.reshape(movie, info['shape'])
    return movie, info


def load_raw_info(path):
    path_base, path_extension = os.path.splitext(path)
    with open(path_base + '.yaml', 'r') as info_file:
        info = yaml.load(info_file)
    info['shape'] = info['shape']
    return info


def load_tif(path):
    info = {}
    with tifffile.TiffFile(path) as tif:
        movie = tif.asarray(memmap=True)
        info['file'] = tif.filename
        if 'datetime' in tif.pages[0].tags:
            info['timestamp'] = tif.pages[0].tags.datetime.value.decode()
        info['data type'] = str(np.dtype(tif.pages[0].dtype))
        info['byte order'] = tif.byteorder
        info['bits per sample'] = tif.pages[0].tags.bits_per_sample.value
    if movie.ndim == 3:
        info['frames'], info['width'], info['height'] = movie.shape
    elif movie.ndim == 2:
        info['frames'] = 1
        info['width'], info['height'] = movie.shape
    elif movie.ndim == 1:
        info['frames'] = info['height'] = 1
        info['width'] = len(movie)
    info['shape'] = [info['frames'], info['width'], info['height']]
    # TODO load baseline, preamp gain, emgain. Dummy numbers for now
    info['baseline'] = 100
    info['preamp gain'] = 2
    info['em realgain'] = 1
    info['quantum efficiency'] = 0.9
    return movie, info


def _to_raw_single(path):
    path_base, path_extension = os.path.splitext(path)
    path_extension = path_extension.lower()
    if path_extension == '.tif' or path_extension == 'tiff':
        movie, info = load_tif(path)
    else:
        raise FileFormatNotSupported("File format must be '.tif' or '.tiff'.")
    raw_file_name = path_base + '.raw'
    movie.tofile(raw_file_name)
    info['generated by'] = 'Picasso ToRaw'
    info['original file'] = info.pop('file')
    info['raw file'] = os.path.basename(raw_file_name)
    with open(path_base + '.yaml', 'w') as info_file:
        yaml.dump(info, info_file, indent=4)


def to_raw(path, verbose=True):
    paths = glob.glob(path)
    n_files = len(paths)
    if n_files:
        for i, path in enumerate(paths):
            if verbose:
                print('Converting file {}/{}...'.format(i + 1, n_files), end='\r')
            _to_raw_single(path)
    else:
        if verbose:
            print('No files matching {}'.format(path))
    return n_files


def save_locs(path, locs, movie_info, identification_parameters):
    np.savetxt(path, locs, fmt='%i\t%f\t%f\t%f\t%f\t%f\t%f\t%f', newline='\r\n', header='\t'.join(locs.dtype.names))
    loc_info = identification_parameters
    loc_info['generated by'] = 'Picasso Localize'
    base, ext = os.path.splitext(path)
    if base.endswith('_locs'):
        base = base[:-5]
    with open(base + '_locs.yaml', 'w') as info_file:
        yaml.dump(movie_info, info_file, indent=4)
        info_file.write('---\n')
        yaml.dump(loc_info, info_file, default_flow_style=False, indent=4)
